using UnityEngine;

public class EnemyHealth : MonoBehaviour
{
    [SerializeField] public int maxHealth = 100;
    [SerializeField] private int currentHealth;
    public GameObject explosionEffect;
    public EnemyHealthBar healthBar;

    private bool isDead = false;

    private void Start()
    {
        currentHealth = maxHealth;

        // ตรวจสอบว่า healthBar มี Component EnemyHealthBar หรือไม่
        healthBar = GetComponent<EnemyHealthBar>();
        if (healthBar == null)
        {
            healthBar = gameObject.AddComponent<EnemyHealthBar>();
        }

        healthBar.maxHealth = maxHealth;
        healthBar.currentHealth = currentHealth;
    }

    public void TakeDamage(int damage)
    {
        currentHealth -= damage;
        healthBar.TakeDamage(damage);

        Debug.Log($"Enemy ({gameObject.name}) took {damage} damage. Current HP: {currentHealth}");

        if (currentHealth <= 0)
        {
            Debug.Log($"Enemy ({gameObject.name}) died!");
            Die();
        }
    }

    private void Die()
    {
        if (isDead) return;

        isDead = true; // ตั้งค่า isDead เป็น true ก่อนทำอย่างอื่น

        // แจ้ง UIManager ว่าศัตรูถูกทำลาย
        if (UIManager.Instance != null)
        {
            UIManager.Instance.EnemyDefeated();
        }
        else
        {
            Debug.LogError("UIManager.Instance is null!");
        }

        // สร้างเอฟเฟกต์ระเบิด (ถ้ามี)
        if (explosionEffect != null)
        {
            Instantiate(explosionEffect, transform.position, transform.rotation);
        }

        // ทำลายวัตถุ
        Destroy(gameObject);
    }
}
